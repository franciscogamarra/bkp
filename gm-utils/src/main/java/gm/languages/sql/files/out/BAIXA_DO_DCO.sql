					SELECT @ID_AGRUPAMENTO, @PROPOSTA, @INSCRICAO, @SALDODIVIDA, @ID_TIPO_TRANSACAO_TRANSFERE_DEBITO_DEV_S, @QUEDA, @SEQUENCIAL_DCO_AMORTIZADO;
				END
				IF (@PRODUTO = 'M')
				BEGIN
					INSERT INTO #TRANSACAO
					(ID_AGRUPAMENTO, PROPOSTA, INSCRICAO, VALOR, ID_TIPO_TRANSACAO, QUEDA, SEQUENCIAL_DCO)
					SELECT @ID_AGRUPAMENTO, @PROPOSTA, @INSCRICAO, @SALDODIVIDA, @ID_TIPO_TRANSACAO_TRANSFERE_DEBITO_DEV_M, @QUEDA, @SEQUENCIAL_DCO_AMORTIZADO;
