		@PROPOSTA, @DATAATUAL, 'N',
		@SALDOATUAL OUTPUT, @SALDOCD OUTPUT,
		@SALDOCAR OUTPUT, @JUROS OUTPUT,
		@CCAR1 OUTPUT, @CCAR2 OUTPUT,
		@CCAR12 OUTPUT, @CCAR22 OUTPUT,
		@CCAR13 OUTPUT, @CCAR23 OUTPUT,
		@IR OUTPUT

