		SELECT VALOR, DATA_MOVIM FROM MOVIMENTO with(nolock)
		WHERE PROPOSTA = @PROPOSTA AND DATA_MOVIM >= @DATADCO AND
		CONVERT(CHAR(6), DATA_MOVIM, 112) = CONVERT(CHAR(6), @DATADCO, 112) AND
