		@TIPORECEBIMENTO = FKTIPORECEBIMENTO
		FROM TB_AGENDA_RECEBIMENTO with(nolock)
		WHERE CODIGOIDENTIFICADOR = @FKAGENDARECEBIMENTO

		IF (@SALDODIVIDA > 0.00 AND @SALDOPROP > 0.00 AND @SALDOATUAL > 0 AND @TIPORECEBIMENTO <> 5) -- 5: Recebimento PLR
		BEGIN
			-- LANCAMENTO DO JUROS DE MORA
			INSERT INTO #TRANSACAO
