create or alter PROCEDURE dbo.BAIXA_DO_DCO
@GERACAO SMALLDATETIME,
@DATADCO SMALLDATETIME,
@BANCO CHAR(3),
@ID_RESPONSAVEL INT,
@INSCRICAO_IN INT = NULL
AS

SET NOCOUNT ON;

DECLARE
@PROPOSTA INT,
@PROPOSTADEBITO INT,
@PROPOSTACAPITAL INT,
@INSCRICAO INT,
@INSCRICAO_TITULAR INT,
@INSCRICAOAVALISTA INT,
@PARCELAS INT,
@PARCELASRESTANTES INT,
@NOVAPARCELA INT,
@DIAS INT,
@PRODUTO CHAR(1),
@CPF_AVALISTA CHAR(14),
@RESPONSAVEL CHAR(10),
@QUEDA CHAR(2),
@COD_MOVIM CHAR(3),
@DESC_OCORRENCIA CHAR(100),

@TOTAL MONEY,
@SALDO MONEY,
@IR MONEY,
@SALDODIVIDA MONEY,
@VALOR MONEY,
@SALDOCD MONEY,
@SALDOCAR MONEY,
@SALDODIS MONEY,
@JUROS MONEY,
@VALORCAPITAL MONEY,
@MONTANTE MONEY,
@VALORATUAL MONEY,
@SALDOATUAL MONEY,
@SALDOPROP MONEY,
@VALORESTORNO MONEY,
@VALORDEPOSITO MONEY,
@DATAATUAL SMALLDATETIME,
@DATAEMP SMALLDATETIME,
@DATAMOVIM SMALLDATETIME,
@DATACARENCIA SMALLDATETIME,
@CONCILIADO CHAR(1),
@SALDO_REMANECENTE MONEY,
@FKAGENDARECEBIMENTO INT,
@TIPORECEBIMENTO INT,
@IS_CONVERSAO_13 BIT;

DECLARE
@ID_SESSAO_TRASACAO_LIBERACAO INT,
@CCAR1 CHAR(224),
@CCAR2 CHAR(210),
@CCAR12 CHAR(224),
@CCAR22 CHAR(210),
@CCAR13 CHAR(224),
@CCAR23 CHAR(210),
@CANAL VARCHAR(20),
@QRYRESULT CHAR(1),
@ID_AGRUPAMENTO INT,
@ID_TIPO_TRANSACAO INT,
@PROPAMORTIZA INT,
@VALOR_LANCADO MONEY,
@PROPOSTAP INT,
@DATA_FIM_TRANSACAO SMALLDATETIME,
@SEQUENCIAL_DCO_AMORTIZADO INT,
@ERROMSG VARCHAR(50)

EXEC ONLYDATE @DATAATUAL OUTPUT

SELECT
@RESPONSAVEL = NOME_OPERADOR
FROM OPERADOR with(nolock)
WHERE COD_OPERADOR = @ID_RESPONSAVEL

SELECT
P.*,
'N' AS ATUALIZA
INTO #TMP_DCO1
FROM DCO P with(nolock)
WHERE P.DATA_COBRANCA = @DATADCO
  AND P.DATA_GERACAO = @GERACAO
  AND P.BANCO = @BANCO
  AND P.INSCRICAO = ISNULL(@INSCRICAO_IN, P.INSCRICAO)
  --Remove queda NG(Não Gerado) da baixa.
  AND P.Queda <> 'NG'

CREATE CLUSTERED INDEX IX_SEQUENCIAL ON dbo.#TMP_DCO1
(
	Sequencial ASC
) WITH (PAD_INDEX = ON, STATISTICS_NORECOMPUTE = OFF, SORT_IN_TEMPDB = OFF, DROP_EXISTING = OFF, ONLINE = OFF, ALLOW_ROW_LOCKS = ON, ALLOW_PAGE_LOCKS = ON, FILLFACTOR = 95)

CREATE NONCLUSTERED INDEX IX_INSCRICAO ON dbo.#TMP_DCO1
(
	INSCRICAO ASC
) WITH (PAD_INDEX = ON, STATISTICS_NORECOMPUTE = OFF, SORT_IN_TEMPDB = OFF, DROP_EXISTING = OFF, ONLINE = OFF, ALLOW_ROW_LOCKS = ON, ALLOW_PAGE_LOCKS = ON, FILLFACTOR = 95)

--PASSA PROCESSADO 'S' DOS DCOS QUE TIVERAM QUEDA E NÃO SERÃO MANDADOS PARA DEBITO DEVOLVILDO PARA
UPDATE #TMP_DCO1
SET PROCESSADO = 'S', ATUALIZA = 'S'
FROM #TMP_DCO1 D
WHERE D.DATA_GERACAO = @GERACAO
  AND D.DATA_COBRANCA = @DATADCO
  AND D.BANCO = @BANCO
  AND D.DATA_VOLTA IS NOT NULL
  AND D.SALDO_DCO = 0
  AND D.PROCESSADO IS NULL
  AND D.QUEDA <> '00'

UPDATE DCO
SET PROCESSADO = T.PROCESSADO,
FkSituacaoRecebimento = 4,
Data_Processamento = @DATAATUAL
FROM DCO D with(nolock) /*gm*/
INNER JOIN #TMP_DCO1 T ON T.SEQUENCIAL = D.SEQUENCIAL AND T.ATUALIZA = 'S'

UPDATE #TMP_DCO1
SET ATUALIZA = 'P'
WHERE ATUALIZA = 'S'

