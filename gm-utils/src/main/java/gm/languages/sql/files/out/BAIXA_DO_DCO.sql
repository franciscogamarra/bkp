DECLARE @ID_SESSAO INT;
DECLARE @ESTACAO_TRABALHO VARCHAR(50)
EXEC ONLYDATE @DATAATUAL OUTPUT

SET @ID_AGRUPAMENTO = 1
SET @CANAL = 'VSICOP'
SET @QRYRESULT = 'N'
SET @ESTACAO_TRABALHO = SUSER_SNAME()

SELECT @CONCILIADO = 'N';

--BUSCA POSSIVEIS DCOS DE PRODUTOS QUE VÃO PARA DEBITO DEVOLVIDO,
--SOMENTE AQUELES QUE SOBRARAM SALDO_DCO SEM AMOTIZAR
--E QUE TODOS OS DCOS JA TENHAM VOLTADOS
DECLARE C CURSOR FAST_FORWARD FOR
SELECT
D.SEQUENCIAL,
D.INSCRICAO,
D.PROPOSTA,
D.PRODUTO,
TOTAL = SUM(ROUND(D.VALOR_DCO, 2)),
D.FKAGENDARECEBIMENTO
FROM #TMP_DCO1 D with(nolock)
WHERE D.DATA_GERACAO = @GERACAO
  AND D.DATA_COBRANCA = @DATADCO
  AND D.DATA_VOLTA IS NOT NULL
  AND D.PRODUTO IN('A','B','E','I','M','S','T','J','V','G','H','Q')
  AND D.BANCO = @BANCO
  AND D.SALDO_DCO <> 0
  AND D.INSCRICAO NOT IN ( -- AS PROPOSTAS DE UM ASSOCIADO SO SERAO ENVIADAS PARA DEBITO DEVOLVIDO QUANDO TODO SEU DCO ESTIVER IMPORTADO
	SELECT
	D1.INSCRICAO
	FROM #TMP_DCO1 D1 with(nolock)
	WHERE D.INSCRICAO = D1.INSCRICAO
	  AND D.DATA_GERACAO = D1.DATA_GERACAO
	  AND D.DATA_COBRANCA = D1.DATA_COBRANCA
	  AND D1.DATA_VOLTA IS NULL
  )
  AND NOT EXISTS ( -- EVITA QUE DCOS COM OCORRENCIA SEJA ENVIADO PARA DEBITO DEV
	SELECT
	O.SEQUENCIAL
	FROM TB_DCO_OCORRENCIA_BAIXA O with(nolock)
	WHERE O.SEQUENCIAL_DCO_AMORTIZADO = D.SEQUENCIAL
  )
GROUP BY D.SEQUENCIAL, D.INSCRICAO, D.PROPOSTA, D.PRODUTO, D.FKAGENDARECEBIMENTO
ORDER BY D.INSCRICAO,
		CASE -- PRIORIDADE DE PRODUTOS PARA AMORTIZAÇÃO
			WHEN D.PRODUTO = 'V' THEN 20
			WHEN D.PRODUTO = 'B' THEN 30
			WHEN D.PRODUTO = 'I' THEN 31
			WHEN D.PRODUTO = 'S' THEN 32
			WHEN D.PRODUTO = 'E' THEN 33
			WHEN D.PRODUTO = 'Q' THEN 33
			WHEN D.PRODUTO = 'G' THEN 34
			WHEN D.PRODUTO = 'T' THEN 40
			WHEN D.PRODUTO = 'J' THEN 41
			WHEN D.PRODUTO = 'M' THEN 42
			WHEN D.PRODUTO = 'F' THEN 43
			WHEN D.PRODUTO = 'C' THEN 50
			WHEN D.PRODUTO = 'H' THEN 51
		END

OPEN C
fetch next from C INTO @SEQUENCIAL_DCO_AMORTIZADO, @INSCRICAO, @PROPOSTA, @PRODUTO, @TOTAL, @FKAGENDARECEBIMENTO
WHILE (@@FETCH_STATUS = 0)
BEGIN

	--UM DCO COM SALDO_DCO = 00 PODE ESTAR NO CURSOS, POIS O SEU VALOR BAIXOU OUTROS DCOS PRIORITARIO,
	--DESTA FORMA ELE VAI PARA DEBITO DEVOLVIDO, MAS A QUEDA É 00
	SELECT
	@QUEDA = MAX(QUEDA)
	FROM DCO with(nolock)
	WHERE DATA_COBRANCA = @DATADCO
	  AND INSCRICAO = @INSCRICAO
	  AND BANCO = @BANCO
	  --Remove queda NG(Não Gerado) da baixa.
	  AND Queda <> 'NG'

	SET @DIA_COBRANCA = NULL
	SET @IS_POSSUI_DEBDEVOLVIDO = NULL

	SET @IS_CONVERSAO_13 = 0

	IF (@PRODUTO = 'H')
	BEGIN
		---- BUSCA DIA COBRANCA ASSOCIADO
		SELECT @DIA_COBRANCA = DiaCobranca FROM SCH_LEGADO.ASSOCIADO with(nolock) WHERE INSCRICAO = @INSCRICAO
		IF (EXISTS(SELECT 1 FROM DEBITODEV with(nolock) WHERE DEBITODEV.MORTO = 'N' and INSCRICAO = @INSCRICAO))
		SET @IS_POSSUI_DEBDEVOLVIDO = 1234567
		ELSE
		begin
			SET @IS_POSSUI_DEBDEVOLVIDO = 0;
		end
	END

	IF (@PRODUTO = 'I')
	BEGIN
		set @IS_CONVERSAO_13 = iif(exists(select 1 FROM SCH_EMPRESTIMO.TB_Conversao13 with(nolock) /*gm*/
		WHERE FK_Conversao13Tipo IN(2,3)
		  AND Inscricao = @INSCRICAO), 1, 0)

		IF (EXISTS(SELECT 1 FROM DEBITODEV with(nolock) WHERE DEBITODEV.MORTO = 'N' and INSCRICAO = @INSCRICAO))
		SET @IS_POSSUI_DEBDEVOLVIDO = 1
		ELSE
		begin
			SET @IS_POSSUI_DEBDEVOLVIDO = 0;
		end

	END;

	IF ((@PRODUTO IN('B','E','Q','M','S','T','J','G'))
	   OR (@PRODUTO = 'H' AND @DIA_COBRANCA IS NOT NULL AND @IS_POSSUI_DEBDEVOLVIDO IS NOT NULL
	  --AND @DIA_COBRANCA NOT IN(19,0)
	  AND @IS_POSSUI_DEBDEVOLVIDO = 1)
	   OR (@PRODUTO = 'H' AND @DIA_COBRANCA IS NOT NULL AND @IS_POSSUI_DEBDEVOLVIDO IS NOT NULL
	  --AND @DIA_COBRANCA NOT IN(19,0)
	  AND EXISTS(SELECT 1 FROM #TRANSACAO with(nolock) WHERE INSCRICAO = @INSCRICAO AND ID_TIPO_TRANSACAO = 251))
	   OR (@PRODUTO = 'I' AND @IS_CONVERSAO_13 = 0)
	   OR (@PRODUTO = 'I' AND @IS_POSSUI_DEBDEVOLVIDO = 1))
	BEGIN

		SET @VALORATUAL = 0
		SET @MONTANTE = 0;
		-- CURSOR COM VERIFICACAO DE LANCAMENTOS DE LIQUIDAÇÃO/AMORTIZAÇÃO

		DECLARE D CURSOR FAST_FORWARD FOR

		SELECT VALOR, DATA_MOVIM
		FROM MOVIMENTO with(nolock)
		WHERE PROPOSTA = @PROPOSTA
		  AND CONVERT(CHAR(8), DATA_MOVIM, 112) >= CONVERT(CHAR(8), @GERACAO, 112)
		  AND CONVERT(CHAR(8), DATA_MOVIM, 112) <= CONVERT(CHAR(8), DATEADD(MONTH, 1, @DATADCO), 112)
		  AND (
			(TIPO = 'E' AND COD_MOVIM IN('503','504','505','506','507','513','622','518','519','600'))
			   OR (TIPO = 'G' AND COD_MOVIM IN('503','504','505','506','507','513','622','518','519','600'))
			   OR (TIPO = 'Q' AND COD_MOVIM IN('503','504','505','506','507','513','622','518','519','600'))
			   OR (TIPO = 'H' AND COD_MOVIM IN('503','504','505','506','507','513','622','518','519','600'))
			   OR (TIPO = 'B' AND COD_MOVIM IN('501','502','503','504','511','518','519','600'))
			   OR (TIPO = 'M' AND COD_MOVIM IN('501','502','503','504','511','518','600'))
			   OR (TIPO = 'S' AND COD_MOVIM IN('503','504','508','509','513','518','600'))
			   OR (TIPO = 'T' AND COD_MOVIM IN('501','502','503','504','518','600'))
			   OR (TIPO = 'I' AND COD_MOVIM IN('501','502','503','504','511','518','519','600'))
			   OR (TIPO = 'J' AND COD_MOVIM IN('501','502','503','504','516','519','600'))
		  )

		OPEN D
		fetch next from D INTO @VALOR, @DATAMOVIM
		WHILE (@@FETCH_STATUS = 0)
		BEGIN
			-- MONTA-SE POR PROPOSTA O TOTAL DE AMORTIZAÇÃO EM DEPOSITOS E DCO
			IF (ISNULL(@VALOR, 0.00) > 0)
			BEGIN
				SELECT @DIAS = DATEDIFF(DAY, @DATADCO, @DATAMOVIM)
				EXEC DEFLACIONA_VALOR @PRODUTO, @VALOR, @DIAS, @DATAMOVIM, 'S', @MONTANTE OUTPUT
				SELECT @VALORATUAL = @VALORATUAL + @MONTANTE;
			END

			fetch next from D INTO @VALOR, @DATAMOVIM;
		END
		CLOSE D
		DEALLOCATE D
		-- @TOTAL --> É O TOTAL DE DCO DO ASSOCIADO POR PROPOSTA
		-- @VALORATUAL --> É O TOTAL DE AMORTIZAÇÃO POR PROPOSTA
		-- TOTAL DO DCO POR PROPOSTA, MENOS O QUE FOI PAGO DENTRO DO PERIODO GERACAO E COBRANCA
		-- @SALDODIVIDA --> VALOR DA DIVIDA DO ASSOCIADO POR PROPOSTA

		SELECT @SALDODIVIDA = ROUND(@TOTAL, 2) - ROUND(@VALORATUAL, 2)
		-- SALDO NA DATA DO DCO DA PROPOSTA
		EXEC SALDO_EMPRESTIMO @PROPOSTA, @PRODUTO, @DATADCO, @SALDOPROP OUTPUT, 'N'
		--CASO VALOR A SER AMORTIZADO SEJA IAGUAL OU MAIOR AO VALOR DEVIDO, SIGNIFICA QUE A PROPOSTA
		--FOI LIQUIDADA, NAO HAVENDO NECESSIDADE DE ENVIAR PARA DEVITO DEVOLVIDO
		IF ((@SALDOPROP - @VALORATUAL) <= 0)
		BEGIN
			SET @SALDODIVIDA = 0.00;
		END

		-- SALDO ATUAL DO EMPRESTIMO
		EXEC SALDO_PROPOSTA
		@PROPOSTA,
		@DATAATUAL,
		'N',
		@SALDOATUAL OUTPUT,
		@SALDOCD OUTPUT,
		@SALDOCAR OUTPUT,
		@JUROS OUTPUT,
		@CCAR1 OUTPUT,
		@CCAR2 OUTPUT,
		@CCAR12 OUTPUT,
		@CCAR22 OUTPUT,
		@CCAR13 OUTPUT,
		@CCAR23 OUTPUT,
		@IR OUTPUT

		-- CRITICA PARA SALDO DA DIVIDA, SALDO PRODUTO E SALDO ATUAL
		IF (@SALDODIVIDA > 0.00 AND @SALDOPROP > 0.00 AND @SALDOATUAL > 0)
		BEGIN
			-- SALDO DE CAPITAL I
			--EXEC SALDO_PRODUTO
			-- 'P',
			-- @INSCRICAO,
			-- @DATAATUAL,
			-- @SALDO OUTPUT,
			-- @SALDOCD OUTPUT,
			-- @SALDOCAR OUTPUT,
			-- @JUROS OUTPUT,
			-- @SALDODIS OUTPUT,
			-- 'N',
			-- 'N'
			SET @SALDODIS = 0.00 -- PARA NAO HAVER AMORTIZACAO/LIQUIDACAO COM CAPITAL -> PEDIDO DA GECRE COM 1608/2015

			--BUSCA VALORES JA LANÇADOS EM OROCESSAMENTO
			SET @VALOR_LANCADO = 0
			SELECT
			@VALOR_LANCADO = ISNULL(SUM(VALOR), 0.00)
			FROM #TRANSACAO
			WHERE ID_TIPO_TRANSACAO IN (
				@ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_E,
				@ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_V,
				@ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_G,
				@ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_B,
				@ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_J,
				@ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_I,
				@ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_Q,
				@ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_H
			)
			  AND INSCRICAO = @INSCRICAO

			SET @VALOR_LANCADO = ISNULL(@VALOR_LANCADO, 0.00)
			-- VALOR DISPONIVEL DE CAPITAL
			SET @VALORCAPITAL = ISNULL(ROUND(@SALDODIS, 2), 0.00) - @VALOR_LANCADO
			IF (@VALORCAPITAL <= 0)
			SET @VALORCAPITAL = 0
			-- CASO O VALOR DE CAPITAL SEJA MAIOR OU IGUAL AO DO EMPRESTIMO NAO PAGO
			IF ((@VALORCAPITAL >= @SALDODIVIDA)
			  AND NOT EXISTS(
				SELECT
				SEQUENCIAL
				FROM MOVIMENTO with(nolock)
				WHERE TIPO = @PRODUTO
				  AND INSCRICAO = @INSCRICAO
				  AND COD_MOVIM =
						CASE
							WHEN @PRODUTO IN('E','G') THEN '504'
							WHEN @PRODUTO IN('B','M','T','I','J') THEN '502'
							WHEN @PRODUTO = 'S' THEN '508'
						END
				  AND RESPONSAVEL = @RESPONSAVEL
				  AND DATA_MOVIM BETWEEN @GERACAO AND @DATADCO
			  ))
			BEGIN
				SET @COD_MOVIM =
						CASE
							WHEN @PRODUTO IN('E','G') THEN '504'
							WHEN @PRODUTO IN('B','M','T','I','J') THEN '502'
							WHEN @PRODUTO = 'S' THEN '508'
						END
				-- INSERE NO MOVIMENTO A AMORTIZACAO DE CAPITAL
				IF (@PRODUTO = 'E')
				SET @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL = @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_E
				ELSE IF (@PRODUTO = 'V')
				SET @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL = @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_V
				ELSE IF (@PRODUTO = 'G')
				SET @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL = @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_G
				ELSE IF (@PRODUTO = 'B')
				SET @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL = @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_B
				ELSE IF (@PRODUTO = 'J')
				SET @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL = @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_J
				ELSE IF (@PRODUTO = 'I')
				SET @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL = @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_I
				ELSE IF (@PRODUTO = 'Q')
				SET @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL = @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_Q
				ELSE IF (@PRODUTO = 'H')
				SET @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL = @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL_H

				EXEC NOVAPROPOSTA 'P', @INSCRICAO, @PROPOSTAP OUTPUT, NULL

				INSERT INTO #TRANSACAO
				(ID_AGRUPAMENTO, PROPOSTA, INSCRICAO, VALOR, ID_TIPO_TRANSACAO, PROPAMORTIZA, SEQUENCIAL_DCO)
				SELECT @ID_AGRUPAMENTO, @PROPOSTAP, @INSCRICAO, @SALDODIVIDA, @ID_TIPO_TRANSACAO_AMORTIZA_COM_CAPITAL, @PROPOSTA, @SEQUENCIAL_DCO_AMORTIZADO
				SET @SALDODIVIDA = 0.00;
			END

			-- RETORNA O TOTAL DEPOSITADO PELO ASSOCIADO, PARA A PROPOSTA, ENTRE A DATA DE GERAÇÃO E DATA DA VOLTA DO DCO
			EXEC BUSCA_DEPOSITO_ASSOCIADO
			@INSCRICAO,
			@GERACAO,
			@DATAATUAL,
			@VALORDEPOSITO OUTPUT,
			@PROPOSTA,
			'C' -- SOMENTE PARA DEPÓSITOS CONFIRMADOS

			-- RETORNA O TOTAL ESTORNADO PELO ASSOCIADO, PARA A PROPOSTA, ENTRE A DATA DE GERAÇÃO E DATA DA VOLTA DO DCO
