DECLARE @ID_SESSAO_TRANSACAO INT;

DECLARE C_TRANSACAO CURSOR FAST_FORWARD FOR

SELECT
DISTINCT ID_AGRUPAMENTO
FROM #TRANSACAO
WHERE ID_TIPO_TRANSACAO <> @GERAR_FORTE_60 --
ORDER BY ID_AGRUPAMENTO

OPEN C_TRANSACAO
fetch next from C_TRANSACAO INTO @ID_AGRUPAMENTO
WHILE (@@FETCH_STATUS = 0)
BEGIN

	SET @ID_SESSAO = NULL;

	DECLARE C_TRANSACAO_DETALHE CURSOR FOR
	SELECT
	SEQUENCIAL_DCO,
	PROPOSTA,
	INSCRICAO,
	VALOR,
	ID_TIPO_TRANSACAO,
	QUEDA,
	PROPAMORTIZA
	FROM #TRANSACAO
	WHERE ID_AGRUPAMENTO = @ID_AGRUPAMENTO
	ORDER BY ID_AGRUPAMENTO

	OPEN C_TRANSACAO_DETALHE
	fetch next from C_TRANSACAO_DETALHE INTO
	@SEQUENCIAL_DCO_AMORTIZADO,
	@PROPOSTA,
	@INSCRICAO,
	@VALOR,
	@ID_TIPO_TRANSACAO,
	@QUEDA,
	@PROPAMORTIZA

	WHILE (@@FETCH_STATUS = 0)
	BEGIN

		IF (@ID_SESSAO IS NULL)
		BEGIN
			EXEC DBO.STP_GLB_INICIA_SESSAO
			@CANAL = @CANAL,
			@INSCRICAO = @INSCRICAO,
			@ESTACAO_TRABALHO = @ESTACAO_TRABALHO,
			@QRYRESULT = 'N',
			@ID_SESSAO = @ID_SESSAO OUTPUT;
		END

		SELECT @ID_SESSAO_TRANSACAO = NULL

		EXEC DBO.STP_GLB_GRAVA_TRANSACAO
		@ID_SESSAO = @ID_SESSAO,
		@ID_TIPO_TRANSACAO = @ID_TIPO_TRANSACAO,
		@VALOR_OPERACAO = @VALOR,
		@INSCRICAO_ORIGEM = @INSCRICAO,
		@INSCRICAO_DESTINO = @INSCRICAO,
		@DT_INICIO_CONTRATO = @DATADCO,
		@ID_RESPONSAVEL = @ID_RESPONSAVEL,
		@PROPOSTA = @PROPOSTA,
		@QUEDA = @QUEDA,
		@PROPOSTA_AMORT = @PROPAMORTIZA,
		@ID_SESSAO_TRANSACAO = @ID_SESSAO_TRANSACAO OUTPUT

		INSERT INTO TB_TRANSACAO_RECEBIMENTO
		VALUES(@ID_SESSAO_TRANSACAO, @SEQUENCIAL_DCO_AMORTIZADO, @SEQUENCIAL_DCO_AMORTIZADO)

		UPDATE #TMP_DCO1
		SET SALDO_DCO = 0,
		ID_SESSAO_ESTORNO = @ID_SESSAO,
		PROCESSADO = 'S'
		WHERE SEQUENCIAL = @SEQUENCIAL_DCO_AMORTIZADO

		fetch next from C_TRANSACAO_DETALHE INTO @SEQUENCIAL_DCO_AMORTIZADO, @PROPOSTA, @INSCRICAO, @VALOR, @ID_TIPO_TRANSACAO, @QUEDA, @PROPAMORTIZA;
	END
	CLOSE C_TRANSACAO_DETALHE
	DEALLOCATE C_TRANSACAO_DETALHE

	IF (@ID_SESSAO IS NOT NULL)
	BEGIN
		BEGIN TRY
			BEGIN TRAN
				SET @DATA_FIM_TRANSACAO = NULL

				EXEC DBO.STP_GLB_FINALIZA_SESSAO
				@ID_SESSAO = @ID_SESSAO,
				@DATA_FIM_TRANSACAO = @DATA_FIM_TRANSACAO OUTPUT,
				@MENSAGEM = NULL

				IF (@DATA_FIM_TRANSACAO IS NOT NULL)
				BEGIN

					UPDATE #TMP_DCO1
					SET ATUALIZA = 'S'
					WHERE ID_SESSAO_ESTORNO = @ID_SESSAO

					UPDATE DCO
					SET
					SALDO_DCO = T.SALDO_DCO,
					PROCESSADO = T.PROCESSADO,
					ID_SESSAO_ESTORNO = T.ID_SESSAO_ESTORNO,
					FkSituacaoRecebimento = 4,
					Data_Processamento = @DATAATUAL
					FROM DCO D with(nolock) /*gm*/
					INNER JOIN #TMP_DCO1 T ON T.SEQUENCIAL = D.SEQUENCIAL AND T.ATUALIZA = 'S'

					UPDATE #TMP_DCO1
					SET ATUALIZA = 'P'
					WHERE ATUALIZA = 'S';

				END
				ELSE
				BEGIN
					SET @ERROMSG = 'SESSAO NAO COMITADA COM SUCESSO ID SESSAO:' + CONVERT(VARCHAR(10), @ID_SESSAO)
					RAISERROR(@ERROMSG, 16, 1);
				END
			COMMIT TRAN;
		END TRY
		BEGIN CATCH
			ROLLBACK
			EXEC STP_GETERRORINFO;
		END CATCH;
	END
	fetch next from C_TRANSACAO INTO @ID_AGRUPAMENTO;
END
CLOSE C_TRANSACAO
DEALLOCATE C_TRANSACAO;

