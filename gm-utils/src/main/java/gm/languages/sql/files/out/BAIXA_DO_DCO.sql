	ID_AGRUPAMENTO INT,
	PROPOSTA INT NOT NULL,
	INSCRICAO INT NOT NULL,
	VALOR MONEY NOT NULL,
	QUEDA CHAR(2),
	ID_TIPO_TRANSACAO INT,
	PROPAMORTIZA INT,
	SEQUENCIAL_DCO INT
);

