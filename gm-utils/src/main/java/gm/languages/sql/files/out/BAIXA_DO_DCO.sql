					SELECT @ID_AGRUPAMENTO, @PROPOSTA, @INSCRICAO, @SALDODIVIDA, @ID_TIPO_TRANSACAO_TRANSFERE_DEBITO_DEV_G, @QUEDA, @SEQUENCIAL_DCO_AMORTIZADO;
				END
				IF (@PRODUTO = 'B')
				BEGIN
					INSERT INTO #TRANSACAO
					(ID_AGRUPAMENTO, PROPOSTA, INSCRICAO, VALOR, ID_TIPO_TRANSACAO, QUEDA, SEQUENCIAL_DCO)
					SELECT @ID_AGRUPAMENTO, @PROPOSTA, @INSCRICAO, @SALDODIVIDA, @ID_TIPO_TRANSACAO_TRANSFERE_DEBITO_DEV_B, @QUEDA, @SEQUENCIAL_DCO_AMORTIZADO;
