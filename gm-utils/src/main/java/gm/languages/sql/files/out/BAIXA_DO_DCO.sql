			SELECT @ID_AGRUPAMENTO, @PROPOSTA, @INSCRICAO, @SALDODIVIDA, @ID_TIPO_TRANSACAO_TRANSFERE_DEBITO_DEV_V, @SEQUENCIAL_DCO_AMORTIZADO, @QUEDA;
		END
		ELSE
		BEGIN
			UPDATE #TMP_DCO1
			SET ATUALIZA = 'S',
			SALDO_DCO = 0.00,
			PROCESSADO = 'S'
			WHERE SEQUENCIAL = @SEQUENCIAL_DCO_AMORTIZADO

			UPDATE DCO
			SET SALDO_DCO = T.SALDO_DCO,
			PROCESSADO = T.PROCESSADO,
			ID_SESSAO_ESTORNO = T.ID_SESSAO_ESTORNO,
			FkSituacaoRecebimento = 4,
			Data_Processamento = @DATAATUAL
			FROM DCO D with(nolock) /*gm*/
			INNER JOIN #TMP_DCO1 T ON T.SEQUENCIAL = D.SEQUENCIAL AND T.ATUALIZA = 'S'

