			SET ATUALIZA = 'P'
			WHERE ATUALIZA = 'S';
		END
	END
	ELSE IF (@PRODUTO in('H','I'))
	BEGIN

		IF (EXISTS(
			SELECT *
			FROM EMPRESTIMO E with(nolock)
			WHERE E.PROPOSTA = @PROPOSTA AND MORTO = 'N'
		))
		BEGIN

			INSERT INTO #TRANSACAO
			(ID_AGRUPAMENTO, PROPOSTA, INSCRICAO, VALOR, ID_TIPO_TRANSACAO, SEQUENCIAL_DCO, QUEDA)
			SELECT
			@ID_AGRUPAMENTO, @PROPOSTA, @INSCRICAO, 0, @GERAR_FORTE_60, @SEQUENCIAL_DCO_AMORTIZADO, @QUEDA;
		END
		ELSE
		BEGIN

			UPDATE #TMP_DCO1
			SET ATUALIZA = 'S',
			SALDO_DCO = 0.00,
			PROCESSADO = 'S'
			WHERE SEQUENCIAL = @SEQUENCIAL_DCO_AMORTIZADO

			UPDATE DCO
			SET SALDO_DCO = T.SALDO_DCO,
			PROCESSADO = T.PROCESSADO,
			ID_SESSAO_ESTORNO = T.ID_SESSAO_ESTORNO,
